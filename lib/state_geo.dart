Map<String, LatLngBounds>? stateBounds;

void buildStateBounds() {
  stateBounds = statePolygons.map((k, v) => MapEntry(k, LatLngBounds.fromPoints(v)));
}

/// Rough check if in/near polygon of states(s)
Iterable<String> checkInsideState(LatLng point, {bool quick = false}) {
  if (stateBounds == null) {
    buildStateBounds();
  }

  // First simple bounding-box check
  final bounds = stateBounds!.entries.where((e) => e.value.contains(point)).map((e) => e.key);

  return quick ? bounds : bounds.where((state) => polygonContainsPoint(point, statePolygons[state]!));
}

